package com.javarush.task.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) {
        for(int i = 9; i > 0; i--)
        {
            System.out.print("8");
        }for(int j = 11; j > 0; j--)
        {
            System.out.println("8");
        }

    }
}
